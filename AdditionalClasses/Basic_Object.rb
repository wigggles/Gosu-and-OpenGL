class Basic3D_Object
  attr_accessor :x, :y, :z
  #---------------------------------------------------------------------------------------------------------
  def initialize(options = {})
    options = {} if options.nil?
    @x = options[:x] || 0.0
    @y = options[:y] || 0.0
    @z = options[:z] || 0.0
  end
end 